# Append the value of current to the end of the list seconds.
# Please use the list.append() method to accomplish that.

seconds = [1.23, 1.45, 1.02]
# seconds variable holds the list of seconds
current = 1.11
# the current seconds time
seconds.append(current)
# appends the current time to the list of seconds
print(seconds)
# print the new list of seconds with the appended item